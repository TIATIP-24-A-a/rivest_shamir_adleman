#include "../src/secure_random.h"
#include <iostream>
#include <vector>
#include <cassert>

void TestSecureRandomGetBytes() {
    try {
        SecureRandom random;
        std::vector<uint8_t> buffer(16, 0);  // 16 bytes, initialized to 0

        random.GetBytes(buffer.data(), buffer.size());

        // Check that not all bytes are zero (astronomically unlikely with true random)
        bool allZeros = true;
        for (uint8_t byte : buffer) {
            if (byte != 0) {
                allZeros = false;
                break;
            }
        }

        assert(!allZeros && "Generated bytes should not all be zero");
        std::cout << "TestSecureRandomGetBytes passed!" << std::endl;
    } catch (const std::exception& e) {
        std::cerr << "TestSecureRandomGetBytes failed with exception: " << e.what() << std::endl;
    }
}

void TestSecureRandomGetRange() {
    try {
        SecureRandom random;
        uint64_t min = 1000;
        uint64_t max = 2000;

        uint64_t value = random.GetRange(min, max);
        assert(value >= min && value <= max && "Generated number should be within range");

        std::cout << "TestSecureRandomGetRange passed!" << std::endl;
    } catch (const std::exception& e) {
        std::cerr << "TestSecureRandomGetRange failed with exception: " << e.what() << std::endl;
    }
}

void TestSecureRandomBigNumberRange() {
    try {
        SecureRandom random;
        BigNumber min("1000000");
        BigNumber max("2000000");

        for (int i = 0; i < 10; i++) {
            BigNumber result = random.GetBigNumberRange(min, max);
            assert(result >= min && result <= max);
        }
        std::cout << "TestSecureRandomBigNumberRange passed!" << std::endl;
    } catch (const std::exception& e) {
        std::cerr << "TestSecureRandomBigNumberRange failed with exception: " << e.what() << std::endl;
    }
}

int main() {
    TestSecureRandomGetRange();
    TestSecureRandomGetBytes();
    TestSecureRandomBigNumberRange();
    return 0;
}